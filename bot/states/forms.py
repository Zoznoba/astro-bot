from aiogram.fsm.state import State, StatesGroup

# ---------------------------------------------------------------------------
# ASYNC УРОК #4: FSM (Finite State Machine) — Машина состояний
# ---------------------------------------------------------------------------
#
# Бот "не помнит" предыдущие сообщения по умолчанию — каждое сообщение
# обрабатывается независимо. FSM позволяет вести диалог по шагам:
#
#   Пользователь: /natal
#   Бот: переводит в состояние waiting_for_birth_date
#   Пользователь: 15.03.1990
#   Бот: видит что юзер в состоянии waiting_for_birth_date → обрабатывает дату
#
# StatesGroup — группа состояний для одного сценария
# State() — отдельный шаг сценария
#
# aiogram хранит состояния в памяти (MemoryStorage по умолчанию).
# Для персистентности между перезапусками — использовать RedisStorage.
# ---------------------------------------------------------------------------


class NatalForm(StatesGroup):
    """Сценарий команды /natal: ввод даты рождения."""
    waiting_for_birth_date = State()


class CompatibilityForm(StatesGroup):
    """Сценарий команды /compatibility: выбор двух знаков зодиака."""
    waiting_for_first_sign = State()
    waiting_for_second_sign = State()

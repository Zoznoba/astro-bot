def get_zodiac_sign(birth_date: date) -> str:
    """Определяет знак зодиака по дате рождения."""
    month, day = birth_date.month, birth_date.day
    for (start_m, start_d), (end_m, end_d), sign in _ZODIAC_RANGES:
        if (month == start_m and day >= start_d) or (month == end_m and day <= end_d):
            return sign
    return "Козерог"


def get_zodiac_emoji(sign: str) -> str:
    """Возвращает эмодзи для знака зодиака."""
    return ZODIAC_SIGNS.get(sign, "⭐")


